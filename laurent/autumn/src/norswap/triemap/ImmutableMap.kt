package norswap.triemap

/**
 * Minimal interface for immutable maps.
 */
interface ImmutableMap<K: Any, V: Any>
{
    // ---------------------------------------------------------------------------------------------

    operator fun get (k: K): V?

    // ---------------------------------------------------------------------------------------------

    fun put (k: K, v: V): ImmutableMap<K, V>

    // ---------------------------------------------------------------------------------------------

    fun remove (k: K): ImmutableMap<K, V>

    // ---------------------------------------------------------------------------------------------

    val size: Int

    // ---------------------------------------------------------------------------------------------

    val empty: Boolean
        get() = size == 0

    // ---------------------------------------------------------------------------------------------
}
